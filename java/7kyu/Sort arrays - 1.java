// Just a simple sorting usage. Create a function that returns the elements of the input-array in a sorted manner.

import java.util.Arrays;

public class SortArray{

  public static int[] sortArray(int[] names){
    Arrays.sort(names);
    return names;
  }
  
  public static long[] sortArray(long[] names){
    Arrays.sort(names);
    return names;
  }
  
  public static Integer[] sortArray(Integer[] names){
    Arrays.sort(names);
    return names;
  }
  
  public static double[] sortArray(double[] names){
    Arrays.sort(names);
    return names;
  }
  
  public static float[] sortArray(float[] names){
    Arrays.sort(names);
    return names;
  }
  
  public static String[] sortArray(String[] names){
    Arrays.sort(names);
    return names;
  }
}